function evaluation_point_index = return_evaluation_point_index (evaluation_point) 

if (evaluation_point == 1)
   evaluation_point_index = 1;
end

if (evaluation_point == 1.5)
   evaluation_point_index = 2;
end

if (evaluation_point == 2)
   evaluation_point_index = 3;
end

if (evaluation_point == 2.5)
   evaluation_point_index = 4;
end

if (evaluation_point == 3)
   evaluation_point_index = 5;
end

if (evaluation_point == 3.5)
   evaluation_point_index = 6;
end

if (evaluation_point == 4)
   evaluation_point_index = 7;
end
